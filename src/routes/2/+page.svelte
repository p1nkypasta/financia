<div class="header">Unit 2 - Supply and Demand</div>

<div class = "content">

<ul class="list-header">
    <li>Law of Supply</li>
    <ul class="list">
        <li>The quantity of a good/service that businesses are willing and able to provide within a range of prices that people would be willing to pay.</li>
        <li>As the price of a good or service increases, the quantity that suppliers offer will increase.</li>
    </ul>
</ul>

<ul class="list-header">
    <li>Law of Demand</li>
    <ul class="list">
        <li>The quantity of a good/service that consumers are willing and able to buy at a particular price.</li>
        <li>The higher the price, the lower the quantity demanded.</li>
    </ul>
</ul>

<ul class="list-header">
    <li>Market Structures</li>
    <ul class="list">
        <li>4 Types</li>
        <ul>
            <li>Pure Perfect Competition</li>
            <ul>
                <li>Every company’s product is basically the same (look, feel, taste).</li>
                <li>Product, price, and promotion are of little importance.</li>
                <li>Compete on place (Distribution).</li>
                <li>Examples: Tomatoes, Apples, Carrots, etc.</li>
                <li>Almost anyone can enter the market with a little bit of cash and a small amount of supplies.</li>
            </ul>
            <li>Monopolistic Competition</li>
            <ul>
                <li>Many sellers competing in the same industry.</li>
                <li>Products can be substituted.</li>
                <li>Must use a marketing mix to differentiate (Price, Product, Promotion).</li>
                <li>Examples: Shoes, clothing stores, restaurants, a mall.</li>
                <li>Harder to get into, but not impossible. Usually requires a loan, a location, and some machinery.</li>
            </ul>
            <li>Oligopoly</li>
            <ul>
                <li>A few companies control the industry (2-7).</li>
                <li>Do not want to compete on price, or else they lose revenue.</li>
                <li>Compete on promotion, product, and place (Brand Loyalty).</li>
                <li>Only a handful of major companies make up the industry.</li>
                <li>Examples: Airlines, banks, auto industry, computer industry, oil, cell phones, internet.</li>
                <li>Very difficult to get into. Would need a lot of money, infrastructure, patents, and factories.</li>
            </ul>
            <li>Monopoly</li>
            <ul>
                <li>Only one firm sells a product or service.</li>
                <li>Often regulated by the government so they cannot raise prices to an insane level.</li>
                <li>Examples: Bell Canada until deregulation, Postal Service, LCBO, Power Suppliers.</li>
                <li>Almost impossible to get into due to cost, infrastructure, and time.</li>
            </ul>
        </ul>
    </ul>
    <ul class="list-header">
        <li>Elasticity</li>
        <ul class="list">
            <li>PED Numbers</li>
            <ul>
                <li>If PED greater than 1, then demand is price elastic (sensitive to price changes).</li>
                <li>If PED = 1, then demand is Unit Elastic.</li>
                <li>If PED less than 1, then demand is price inelastic (not sensitive to price changes).</li>
                <li>Price Elasticity of Demand = Percentage Change in Quantity Demanded ÷ Percentage Change in Price.</li>
            </ul>
            <li>Factors that Affect Elasticity</li>
            <ul>
                <li>Luxury vs. Necessity</li>
                <ul>
                    <li>Necessities tend to be inelastic.</li>
                    <li>Luxuries tend to be elastic.</li>
                </ul>
                <li>Number of Substitutes</li>
                <ul>
                    <li>Many substitutes = elastic.</li>
                    <li>Few substitutes = inelastic.</li>
                </ul>
                <li>Percentage of Household Budget</li>
                <ul>
                    <li>Small percentage of budget = inelastic.</li>
                    <li>Large percentage of budget = elastic.</li>
                </ul>
                <li>Time Horizon</li>
                <ul>
                    <li>Short Run - Inelastic.</li>
                    <li>Long Run - Elastic.</li>
                </ul>
            </ul>
            <li>Elastic vs. Inelastic</li>
            <ul>
                <li>A product is considered elastic if the quantity demanded of the product changes drastically when its price increases or decreases.</li>
                <li>A product is considered inelastic if the quantity demanded of the product changes very little when its price fluctuates.</li>
            </ul>
        </ul>
    </ul>
    
    <ul class="list-header">
        <li>Complement and Substitute Goods</li>
        <ul class="list">
            <li>Complement goods - goods that go along with the product.</li>
            <li>Substitute goods - goods that replace the product.</li>
        </ul>
    </ul>
    
    <ul class="list-header">
        <li>Equilibrium, Shortage, Surplus</li>
        <ul class="list">
            <li>When supply matches demand.</li>
            <li>Shortage - supply is less than demand.</li>
            <li>Surplus - demand is less than supply.</li>
        </ul>
    </ul>
    
    <ul class="list-header">
        <li>Graphing Supply and Demand</li>
        <ul class="list">
            <li>Show changes (increase/decrease) to supply and/or demand.</li>
            <li>Normal goods are products and services that see a rise in demand when incomes rise.</li>
            <li>Inferior goods are products and services that see a decrease in demand as incomes rise.</li>
        </ul>
    </ul>
</ul>



